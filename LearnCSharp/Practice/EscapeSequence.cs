namespace LearnCSharp.Practice
{
    static class EscapeSequence
    {
        public static void EscapeSequenceMethod()
        {
            // Tab Space...
            Console.WriteLine("\tHello Sabari Vivek...");

            // New Line...
            Console.WriteLine("Hello Sabari\n Vivek...");

            // Back Space...
            Console.WriteLine("Hello Sabari\b\b\b Vivek...");

            // Double Quotes...
            Console.WriteLine("\"Hello Sabari Vivek...\"");
        }
    }
}
